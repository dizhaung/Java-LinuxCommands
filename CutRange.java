public class CutRange {
    private Integer start;
    private Integer end;

    public CutRange(Integer start, Integer end) {
        this.start=start;
        this.end=end;
    }

    public Integer getStart() {
        return start;
    }

    public Integer getEnd() {
        return end;
    }
}
